# Operando as strings

# ESCREVENDO TEXTO EM BLOCO!
texto_em_bloco = """BEM VINDO(A)
É um prazer tê-lo(a) aqui em nosso site!"""
print(texto_em_bloco)

# METODOS DAS STRINGS

# Usando o ".strip()", os espaços em cada extremidade serão ignorados
# EX:
Nome1 = "  Thiago Antonio Costa Amorim  "
print(Nome1.strip())

# Usando o ".lower()" todo o texto será convertido para letras minúsculas
# EX:
Nome2 = "Thiago Antonio Costa Amorim"
print(Nome2.lower())

# Usando o ".upper()" todo o texto ficará em letras maiúsculas
# EX:
Nome3 = "Thiago Antonio Costa Amorim"
print(Nome3.upper())

